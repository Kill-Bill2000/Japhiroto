    private void bestellungAktualisieren() {
        DefaultTableModel model;
        model = (DefaultTableModel) tblArtikel.getModel();
        model.setRowCount(0);
        for (int i = 0; i < artikel.size(); i++) {
            Artikel aktuell;
                
            aktuell = artikel.get(i);
                
            model.addRow(new Object[]{"", aktuell.getName()});
        }
    }

