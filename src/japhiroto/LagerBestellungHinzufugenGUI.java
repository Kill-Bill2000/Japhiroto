    private void btnDiaSuchenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiaSuchenActionPerformed
        try {
            String gesuchteArtNr;
            ArrayList<Artikel> gesuchteArt;
            DefaultTableModel model;
            
            gesuchteArtNr = txfDiaArtikelNr.getText();
            model = (DefaultTableModel) tblGesuchteArtikel.getModel();
            gesuchteArt = verwaltung.getArtikelListeFromNummer(gesuchteArtNr);
            
            gesuchteLeeren();
            for (int i = 0; i < gesuchteArt.size(); i++) {
                Artikel aktuell;
                
                aktuell = gesuchteArt.get(i);
                
                model.addRow(new Object[]{aktuell.getArtikelNummer(), aktuell.getName()});
            }
        } catch (SQLException ex) {
            getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Ein Datenbank-Fehler ist aufgetreten.", "SQL-Fehler", JOptionPane.ERROR_MESSAGE);
        } catch (NullPointerException ex) {
            getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Keinen Artikel gefunden", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnDiaSuchenActionPerformed

    private void btnDiaArtikelHinzufugenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDiaArtikelHinzufugenActionPerformed
        String gesuchteArtNr;
        int select;
        DefaultTableModel model;
        
        select = tblGesuchteArtikel.getSelectedRow();
        model = (DefaultTableModel) tblGesuchteArtikel.getModel();
        
        
        if (select == -1) {
           getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Es wurde kein Artikel ausgewählt.", "Auswahl-Fehler", JOptionPane.ERROR_MESSAGE);
        }
        else {
            try {
                String artNr;
                Artikel e;
                
                artNr = model.getValueAt(select, 0).toString();
                e = verwaltung.getArtikel(artNr);
                
                artikel.add(e);
            } catch (SQLException ex) {
                getToolkit().beep();
                JOptionPane.showMessageDialog(this, "Ein Datenbank-Fehler ist aufgetreten.", "SQL-Fehler", JOptionPane.ERROR_MESSAGE);
            }
            diaArtikelHinzufugen.setVisible(false);
            gesuchteLeeren();
            bestellungAktualisieren();
        }
    }//GEN-LAST:event_btnDiaArtikelHinzufugenActionPerformed

    private void btnBestellungHinzufugenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBestellungHinzufugenActionPerformed
        try {
            DefaultTableModel model;
            String bestellNr, lieferant;
            
            model = (DefaultTableModel) tblArtikel.getModel();
            bestellNr = txfBestellNummer.getText();
            lieferant = txfLieferant.getText();
            
            ArrayList<Artikel> test = artikel;
            
            for (int i = 0; i < artikel.size(); i++) {
                int anzahl;
                
                anzahl = Integer.valueOf(model.getValueAt(i, 0).toString());
                
                artikel.get(i).setAnzahl(anzahl);
            }
            bestellung.artikelBestellen(artikel, bestellNr, lieferant);
        } catch (SQLException ex) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Ein Datenbank-Fehler ist aufgetreten.", "SQL-Fehler", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException ex) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Falsche Stückzahl der Artikel eingegeben.", "Format-Fehler", JOptionPane.ERROR_MESSAGE);
        } catch (NullPointerException ex) {
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Ein Fehler ist aufgetreten:\n" + ex.getMessage(), "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBestellungHinzufugenActionPerformed

